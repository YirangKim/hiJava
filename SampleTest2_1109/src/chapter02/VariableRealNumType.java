package chapter02;

public class VariableRealNumType {

	public static void main(String[] args) {
		//실수 데이터 저장  타입 : float double
		
		// 데이터 1.23456789f을 저장
		float num1 = 1.234566767f;
		// 데이터 1.234567891234567까지의 근사치 값을 저장
		double num2 = 1.23456789123456789;
		
	
		System.out.println(num1);
		System.out.println(num2);

	}

}
