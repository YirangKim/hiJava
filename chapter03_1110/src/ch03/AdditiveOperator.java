package ch03;

public class AdditiveOperator {

	public static void main(String[] args) {
		
		// 초기화 O,
		int num1 = 5;
		int num2 = +num1;
		int num3 = -num1;
		
		// println 메소드 ("매개값") "" 매개값 출력 
		// + 붙여서 
		System.out.println("num => " + num1);
		System.out.println("num => " + num2);
		System.out.println("num => " + num3);

	}

}
